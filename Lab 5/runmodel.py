for line in f.readlines():
    if(len(line)<1):
        continue
    labels.append(line.split(' ')[1].strip())


while(True):
    draw.rectangle((0, 0, width, height), outline=0, fill="#FFFFFF")
    if webCam:
        ret, img = cap.read()

    rows, cols, channels = img.shape
